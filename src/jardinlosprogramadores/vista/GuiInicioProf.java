    public void llenarDatos() {
        nombre.setText(profesor.getNombre() + " " + profesor.getApellidos());
        Id.setText(profesor.getId());
        tDocument.setText(profesor.gettDocumento());
        nGrupo.setText(String.valueOf(profesor.getnGrupo()));
        //llenar tabla ninnos
        for (int i = 0; i < profesor.getGrupo().getNinnos().size(); i++) {
            Ninno ninno = profesor.getGrupo().getNinnos().get(i);
            String[] vNinno = {ninno.gettDocumento(), ninno.getId(), ninno.getNombre() + " " + ninno.getApellidos()};
            DefaultTableModel tabla = (DefaultTableModel) tablaNinnosProf.getModel();
            tabla.addRow(vNinno);
        }
        totalNinnosProf.setText(String.valueOf(profesor.getGrupo().getNinnos().size()));

        //llenar tabla logros
        for (int i = 0; i < profesor.getGrupo().getLogros().size(); i++) {
            Logro logro = profesor.getGrupo().getLogros().get(i);
            String[] vLogro = {logro.getTitulo(), logro.getDescripcion()};
            DefaultTableModel tabla = (DefaultTableModel) tablaLogros.getModel();
            tabla.addRow(vLogro);
        }
        cLogros.setText(String.valueOf(profesor.getGrupo().getLogros().size()));
    }

