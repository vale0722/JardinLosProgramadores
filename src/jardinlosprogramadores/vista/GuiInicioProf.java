    private void verNinnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verNinnoActionPerformed
        DefaultTableModel model = (DefaultTableModel) tablaNinnosProf.getModel();
        int a = tablaNinnosProf.getSelectedRow();
        Ninno ninno = null;
        if (a < 0) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila de la tabla");
        } else {
            for (int i = 0; i < profesor.getGrupo().getNinnos().size(); i++) {
                if (profesor.getGrupo().getNinnos().get(i).getId().equals(String.valueOf(tablaNinnosProf.getValueAt(a, 1)))) {
                    ninno = profesor.getGrupo().getNinnos().get(i);
                }
            }
            if (ninno != null) {
                GuiNinno infoNinnoProf = new GuiNinno();
                infoNinnoProf.setProfesor(profesor);
                infoNinnoProf.setNinno(ninno);
                infoNinnoProf.getrGenerados().setText(" " + ninno.getReporte().size());
                infoNinnoProf.setFrameProf(getFrame());
                infoNinnoProf.setFrameInicio(getFrameInicio());
                infoNinnoProf.setNombre(ninno.getNombre() + " " + ninno.getApellidos());
                infoNinnoProf.setDocumento(ninno.gettDocumento() + ". " + ninno.getId());
                infoNinnoProf.setEdad(ninno.getEdad());
                infoNinnoProf.setCondicion(ninno.getCondEspeciales());
                infoNinnoProf.setNombreA(ninno.getPariente().getNombre() + " " + ninno.getPariente().getApellidos());
                infoNinnoProf.setDocumentoA(ninno.getPariente().gettDocumento() + ". " + ninno.getPariente().getId());
                infoNinnoProf.setContactoA(ninno.getPariente().getTelefono() + " - " + ninno.getPariente().getCelular());
                infoNinnoProf.setCorreoA(ninno.getPariente().getCorreo());
                infoNinnoProf.setPromedio("" + ninno.getPromedio());
                infoNinnoProf.setParentezco(ninno.getPariente().getParentezco());
                infoNinnoProf.getAgregarLogroNinno().setVisible(true);
                infoNinnoProf.getAgregarLogroNinno().setEnabled(true);
                infoNinnoProf.getGenerarReporte().setVisible(true);
                infoNinnoProf.getGenerarReporte().setEnabled(true);
                //llenar tabla Logros ninno
                for (int i = 0; i < ninno.getLogrosNinno().size(); i++) {
                    LogroNinno logroninno = ninno.getLogrosNinno().get(i);
                    String[] vLNinno = {logroninno.getLogro().getTitulo(), logroninno.getLogro().getDescripcion(), logroninno.getAlcance(), String.valueOf(logroninno.getNota())};
                    DefaultTableModel tabla = (DefaultTableModel) infoNinnoProf.getTablaLogros().getModel();
                    tabla.addRow(vLNinno);
                }
                infoNinnoProf.setVisible(true);
            }
        }
    }//GEN-LAST:event_verNinnoActionPerformed

